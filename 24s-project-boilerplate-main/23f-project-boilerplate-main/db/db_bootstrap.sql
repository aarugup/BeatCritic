create table  beatcritic.Users (
 userID int not null  AUTO_INCREMENT,
 fName varchar(100) not null,
 mName varchar(100),
 lName varchar(100) not null,
 email varchar(100) not null,
 Phoneno varchar(100),
 primary key (userID),
unique key (Phoneno)
 );

create table  beatcritic.Moderators (
   modID int not null  AUTO_INCREMENT ,
   fName varchar(100) not null,
   mName varchar(100),
   lName varchar(100) not null,
   email varchar(100) not null,
   Phoneno varchar(100),
 primary key (modID),
unique key (Phoneno)
);

create table beatcritic.Followers (
   FollowID int not null  AUTO_INCREMENT,
   fName varchar(100) not null,
   mName varchar(100),
   lName varchar(100) not null,
   email varchar(100) not null,
   Phoneno varchar(100),
 primary key (FollowID),
unique key (Phoneno)

);

create table  beatcritic.Artists (
   ArtistID int not null  AUTO_INCREMENT,
   fName varchar(100) not null,
   mName varchar(100),
   lName varchar(100) not null,
   email varchar(100) not null,
   Phoneno varchar(100),
 primary key (ArtistID),
unique key (Phoneno)
);

create table  beatcritic.Genre (
   GenreID int not null  AUTO_INCREMENT,
   GenreDescription varchar(100) not null,
 primary key (GenreID)
);

create table beatcritic.Songs (
   SongID int not null  AUTO_INCREMENT,
   SongDescription varchar(100) not null,
   ArtistID int not null references Artists(ArtistID),
   GenreID int not null references Genre(GenreID),
 primary key (SongID)
);

create table beatcritic.Ratings (
   RateID int not null  AUTO_INCREMENT,
   songID int not null references Songs(songID),
   userID int not null references Users(userID),
   rating int check(rating<=5 and rating>=1),
 primary key (RateID)
);

create table beatcritic.Reviews (
   ReviewID int not null  AUTO_INCREMENT,
   songID int not null references Songs(songID),
   modID int references Moderators(modID),
   userID int not null references Users(userID),
   review_txt varchar(100) not null,
 primary key (ReviewID)
);
create table beatcritic.Comments (
   CommentID int not null  AUTO_INCREMENT,
   reviewID int not null references Reviews(ReviewID),
   followID int references Followers(FollowID),
   modID int references Moderators(modID),
   Comment_txt varchar(100) not null,
 primary key (CommentID)
);
create table beatcritic.Likes (
   LikeID int not null  AUTO_INCREMENT,
   reviewID int not null references Reviews(ReviewID),
   followID int references Followers(FollowID),
   like_boolean boolean,
 primary key (LikeID)
);
Create table beatcritic.notifications(
  NotifID int not null  AUTO_INCREMENT,
  userID int references Users(userID),
  ArtistID int references Artists(ArtistID),
   Notification_txt varchar(100) not null,
 primary key (NotifID)
);
Create table beatcritic.Reports(
  RepID int not null  AUTO_INCREMENT,
  modID int references Moderators(modID),
  reviewID int not null references Reviews(ReviewID),
  userID int  references Users(userID),
  followID int references Followers(FollowID),
  Report_txt varchar(100) not null,
 primary key (RepID)
);
